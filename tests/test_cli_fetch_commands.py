def test_no_local_csv_fallback_introduced(tmp_path):
    """Test that no local CSV fallback behavior is introduced."""
    local_csv = tmp_path / "project_index.csv"
    
    # Create an old CSV with mismatched metadata
    csv_content = "Year,Month,OppID\n2024,6,OPP001\n"
    with open(local_csv, "w", encoding="utf-8") as f:
        f.write(csv_content)
    
    # Create metadata with wrong hash
    meta_path = Path(f"{local_csv}.meta.json")
    metadata = {
        "source": "google_sheet_project_index",
        "sheet_id": "test_sheet_id",
        "gid": "123",
        "retrieved_at": "2026-06-18T10:00:00Z",
        "local_csv_path": str(local_csv),
        "row_count": 2,
        "content_hash": "wrong_hash_that_doesnt_match"
    }
    
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(metadata, f)
    
    # Run index-projects with --skip-fetch
    # Should fail because metadata hash doesn't match
    result = runner.invoke(app, [
        "index-projects",
        "--skip-fetch",
        "--force"
    ])
    
    # Should fail with metadata mismatch error, not silently use old CSV
    assert result.exit_code != 0
    assert "mismatch" in result.output.lower() or "metadata" in result.output.lower()
